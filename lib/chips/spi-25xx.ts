export function getSPINORByName(name: string): SPINORSpec | undefined {
  return spiNORs.find(chip => chip.name === name);
}

export function getSPINORsByFamily(family: string): SPINORSpec[] {
  return spiNORs.filter(chip => chip.family === family);
}
